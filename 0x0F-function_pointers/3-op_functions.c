#include "3-calc.h"

/**
 * op_add - addition
 *
 * @a: 1st int
 * @b: 2nd int
 *
 * Return: sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtraction
 *
 * @a: 1st int
 * @b: 2nd int
 *
 * Return: difference
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication
 *
 * @a: 1st int
 * @b: 2nd int
 *
 * Return: result
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divsion
 *
 * @a: 1st int
 * @b: 2nd int
 *
 * Return: result
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - remainder of division
 *
 * @a: 1st int
 * @b: 2nd int
 *
 * Return: result
 */

int op_mod(int a, int b)
{
	return (a % b);
}
